import cv2
import numpy as np
from skimage import exposure

# Load images
img = cv2.imread('/content/Scene_2.png')
gt = cv2.imread('/content/GT2.png', 0)  # ground truth (binary)

# --- 1. Canal vert ---
green = img[:, :, 1]

# --- 2. CLAHE ---
clahe = cv2.createCLAHE(clipLimit=2.0, tileGridSize=(10,10))
enhanced = clahe.apply(green)

# --- 3. Filtre médian ---
blur = cv2.medianBlur(enhanced, 5)

# --- 4. Top-hat (isoler zones très brillantes) ---
kernel = cv2.getStructuringElement(cv2.MORPH_ELLIPSE, (35,35))
tophat = cv2.morphologyEx(blur, cv2.MORPH_TOPHAT, kernel)

# --- 5. Seuillage Otsu ---
_, thresh = cv2.threshold(tophat, 0, 255, cv2.THRESH_BINARY + cv2.THRESH_OTSU)

# --- 6. Morphologie ---
kernel_small = np.ones((8,8), np.uint8)
clean = cv2.morphologyEx(thresh, cv2.MORPH_OPEN, kernel_small)
clean = cv2.morphologyEx(clean, cv2.MORPH_CLOSE, kernel_small)

# --- 7. Filtrage des composantes ---
num_labels, labels, stats, centroids = cv2.connectedComponentsWithStats(clean)

mask = np.zeros_like(clean)

best_score = 0
best_label = 0

for i in range(1, num_labels):
    area = stats[i, cv2.CC_STAT_AREA]

    if area < 500 :  # enlever bruit
        continue

    # Bounding box
    x, y, w, h, _ = stats[i]

    # Circularité approx (rapport largeur/hauteur)
    ratio = w / h if h != 0 else 0

    # Score simple : aire + circularité
    score = area * (1 - abs(1 - ratio))

    if score > best_score:
        best_score = score
        best_label = i

mask[labels == best_label] = 255

import matplotlib.pyplot as plt

plt.figure(figsize=(12,4))

plt.subplot(1,4,1)
plt.title("Canal vert")
