import numpy as np
import cv2 # Import cv2 for image resizing

def compute_metrics(pred, gt):
    # Binarisation (0 ou 1)
    pred = (pred > 0).astype(np.uint8)
    gt = (gt > 0).astype(np.uint8)

    # Calcul TP, FP, FN
    TP = np.sum((pred == 1) & (gt == 1))
    FP = np.sum((pred == 1) & (gt == 0))
    FN = np.sum((pred == 0) & (gt == 1))

    # Métriques
    IoU = TP / (TP + FP + FN + 1e-8)
    Dice = (2 * TP) / (2 * TP + FP + FN + 1e-8)
    Precision = TP / (TP + FP + 1e-8)
    Recall = TP / (TP + FN + 1e-8)

    return IoU, Dice, Precision, Recall

# Resize gt to match mask's shape before computing metrics
# cv2.resize expects (width, height) for dsize, so mask.shape[::-1] or (mask.shape[1], mask.shape[0])
if gt.shape != mask.shape:
    gt_resized = cv2.resize(gt, (mask.shape[1], mask.shape[0]), interpolation=cv2.INTER_NEAREST)
else:
    gt_resized = gt

iou, dice, precision, recall = compute_metrics(mask, gt_resized)

print(f"IoU       : {iou:.4f}")
print(f"Dice      : {dice:.4f}")
print(f"Precision : {precision:.4f}")
print(f"Recall    : {recall:.4f}")